
# 3️⃣ List vs Tuple vs Set vs Dictionary এর পার্থক্য?

# DataType	      Mutable	      Ordered	      Duplicates_Allowed
# List []	          Yes	           Yes	         Yes
# Tuple ()            No	            Yes	      Yes
# Set {}	            Yes	            No       	No
# Dictionary	   Yes	            Yes	      Keys unique

# List 
my_list = [1, 2, 3, 4, 5]

# Tuple
my_tuple = (1, 2, 3, 4, 5)

# Set 
my_set = {1, 2, 3, 4, 5, 5} # Deuplicates not allowed, so it will only store one '5

# Dictionary 
my_dict = {'name': 'Mamun' , 'age': 27, 'city': 'Bashundhara'}

